"""Utility I/O functions."""

# -----------------------------------------------------------------------------
# Imports
# -----------------------------------------------------------------------------
import json


# -----------------------------------------------------------------------------
# JSON functions
# -----------------------------------------------------------------------------
def load_json(path):
    with open(path, 'r') as f:
        return json.load(f)
    
def save_json(path, d):
    with open(path, 'w') as f:
        json.dump(d, f, indent=4)

